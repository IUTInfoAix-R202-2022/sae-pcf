package fr.univ_amu.iut.DAO.entities;

public class Degree {
    private int id;
    private String name;

    public Degree(){

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String Name) {
        this.name = Name;
    }
}
