package fr.univ_amu.iut.DAO.entities;

public class Discipline {
    private int id;
    private String name;

    public Discipline(){

    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
